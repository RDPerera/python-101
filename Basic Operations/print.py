# Print function
print("Hello, World!")

# print() function can take multiple arguments
print("Hello", "World", "!")

# print() function can take multiple arguments of different types
print("Hello", 1, "World", 2, "!")

# print() function can have a custom separator 
print("Hello", "World", "!", sep=",")

# print() function can have a custom end
print("Hello", "World", "!", end=" ")
print("Hello", "World", "!")

# print() function can have a custom separator and end
print("Hello", "World", "!", sep=",", end=" ")
print("Hello", "World", "!")

