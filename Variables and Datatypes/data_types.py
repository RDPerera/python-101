#Data Types
#Python has the following data types built-in by default, in these categories:
# 1. Strings x="Hello World" x='Hello World' x='''Hello World''' x="""Hello World"""
# 2. Numbers (integers, floating point numbers) x=10(int) x=10.5(float)
# 3. List x=[1,2,3,4,5] x=['apple','banana','cherry'] x=[1,2,'apple','banana'] x=[1,2,[3,4,5],6,7]
# 4. Tuple x=(1,2,3,4,5) x=('apple','banana','cherry') x=(1,2,'apple','banana') x=(1,2,[3,4,5],6,7)
# 5. Set x={1,2,3,4,5} x={'apple','banana','cherry'} x={1,2,'apple','banana'} x={1,2,{3,4,5},6,7}
# 6. Dictionary x={'name':'John','age':25} x={1:'apple',2:'banana',3:'cherry'} x={1:2,3:4,5:6}
# 7. Boolean x=True x=False
# 8. None x=None
# 9. Complex x=1+2j